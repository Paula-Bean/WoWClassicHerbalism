herbs = {} # Mapping of herb name => (orange, yellow, green, gray skill level, where)

for line in info.splitlines():
    line = line.strip()
    if line == "":
        parsephase = 0
        continue
    if parsephase == 0:
        skill = line
        parsephase = 1
    elif parsephase == 1:
        herb = line
        skills = skill.split(" ")
        orange, yellow, green, gray = [int(skill) for skill in skills]
        herbs[herb] = [orange, yellow, green, gray, ""]
        parsephase = 2
    elif parsephase == 2:
        herbs[herb][4] = line
        parsephase = 3


print(f"\nFeasible herbs for a leveling herbalist at level {herbalist_skill_level}:\n")

for herb, skills in sorted(herbs.items()):
    orange, yellow, green, gray, where = skills
    if herbalist_skill_level < orange:
        continue
    if herbalist_skill_level >= gray:
        continue

    color = ""
    if  herbalist_skill_level < yellow:
        color = "orange"
    elif herbalist_skill_level < green:
        color = "yellow"
    elif herbalist_skill_level < gray:
        color = "green"

    print(f"{herb} ({color}): {orange}, {yellow}, {green}, {gray}")

print("\nWhere it can be found:\n")

for herb, skills in sorted(herbs.items()):
    orange, yellow, green, gray, where = skills
    if herbalist_skill_level < orange:
        continue
    if herbalist_skill_level >= gray:
        continue
    print(f"{herb}: {where}\n")

